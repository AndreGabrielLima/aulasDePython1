cupom = str(input('Digite aqui seu cupom: '))

# Variável recebendo o cupom do usuário

if (cupom == 'FUCTURA1'):

    # Condição para caso o cupom seja FUCTURA1

    print('Você recebeu 15% de desconto. Parabéns!!')

    # Interação informando que o cupom foi validado e recebeu 15% de desconto

elif (cupom == 'FUCTURA2'):

    # Condição para caso o cupom seja FUCTURA2

    print('Você recebeu 10% de desconto. Parabéns!!')

    # Interação informando que o cupom foi validado e recebeu 10% de desconto

else:

    # Condição para caso o cupom não seja validado

    print('Esse cupom não é válido! Não tem desconto dessa vez')

    # Interação informando que o cupom não é válido e dizendo que não tem desconto