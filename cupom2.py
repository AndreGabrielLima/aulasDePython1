cupom = str(input('Digite aqui o seu cupom: '))

# Variável para receber o cupom do usuário

if (cupom == 'FUCTURA1' or cupom == 'FUCTURA2'):

    # Condição para o cupom caso seja FUCTURA1 ou FUCTURA2. O processamento

    print('Você recebeu 15% de desconto. Parabéns!!')

    # Interação informando que o cupom está válido, e recebeu 15% de desconto

else:

    # Condição para caso o cupom não seja válido

    print('Este cupom não é válido! Não tem desconto dessa vez')

    # Interação informando que o cupom não está válido e que não tem desconto
